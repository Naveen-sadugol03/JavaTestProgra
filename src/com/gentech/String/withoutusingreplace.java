package com.gentech.String;

public class withoutusingreplace {

	public static void main(String[] args) {
		String old="Gentech";
		String newS="SG Institute";
		String result=Replace(old,newS);
		System.out.println(result);

	}
	static String Replace(String old,String newS) {
		old="";
		return old+newS;
		
	}

}
